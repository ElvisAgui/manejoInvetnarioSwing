    private void InvetarioPjTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InvetarioPjTableMouseClicked
        // TODO add your handling code here:
        int column = this.InvetarioPjTable.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / this.InvetarioPjTable.getRowHeight();
        if (row < this.InvetarioPjTable.getRowCount() && row >= 0 && column < this.InvetarioPjTable.getColumnCount() && column >= 0) {
            Object value = this.InvetarioPjTable.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton btn = (JButton) value;
                producto = productoConsul.productoEdit(btn.getName(), categoriaConsul);
                this.nameEditionjTextField.setText(producto.getNombre());
                this.descripEditonjTextArea.setText(producto.getDescripcion());
                this.cantidadEdtionjTextField.setText(producto.getCantidad() + "");
                this.precioEditojTextField.setText("" + producto.getPrecio());
                categoriaConsul.enlistarCategorias(categoriasProductojComboBox, producto.getCategorias());
                JOptionPane.showMessageDialog(this, "Producto en el área de edicion");
            }
        }
    }//GEN-LAST:event_InvetarioPjTableMouseClicked

    private void actulizarProjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actulizarProjButtonActionPerformed
        // TODO add your handling code here:
        if (producto != null) {
            if (nameEditionjTextField.getText().equals("") || cantidadEdtionjTextField.getText().equals("") || precioEditojTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Llenar los campos obligatorios");
            } else {
                String nameActual = producto.getNombre();
                producto.setCantidad(convertidorInt(cantidadEdtionjTextField.getText()));
                producto.setDescripcion(descripEditonjTextArea.getText());
                producto.setNombre(nameEditionjTextField.getText());
                producto.setPrecio(convertidorDouble(precioEditojTextField.getText()));
                productoConsul.updateProducto(producto, nameActual);
                manejador.manejoInventarioProductos(productoConsul.listaProductos(true, true), InvetarioPjTable);

            }
        }

    }//GEN-LAST:event_actulizarProjButtonActionPerformed

