    private boolean isAscendente() {
        switch (this.desAscjComboBox.getSelectedItem().toString()) {
            case "Ascendente cantidad":
                return true;
            case "Ascendente precio":
                return true;
            default:
                return false;
        }
    }

    private boolean isCantidad() {
        switch (this.desAscjComboBox.getSelectedItem().toString()) {
            case "Ascendente cantidad":
                return true;
            case "Descendente cantidad":
                return true;
            default:
                return false;
        }
    }

    private String categoriaSelect() {
        if (this.CategoriasjComboBox.getSelectedItem().toString().equals("Categorias")) {
            return "";
        } else {
            return this.CategoriasjComboBox.getSelectedItem().toString();
        }
    }

    private boolean insertCategoria() {
        if (this.CategoriasjComboBox.getSelectedItem().toString().equals("Categorias")) {
            return false;
        } else {
            return true;
        }
    }

    private int convertidorInt(String cantidad) {
        int num = 0;
        try {
            num = Integer.parseInt(cantidad);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La cantidad se ha registrado en 0");
        }

        return num;
    }

    private double convertidorDouble(String cantidad) {
        double num = 0.0;
        try {
            num = Double.parseDouble(cantidad);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio se ha registrado en 0");
        }

        return num;
    }

    private void soloNumDecimal(JTextField text) {
        text.addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c) && c != '.') {
                    e.consume();
                }
                if (c == '.' && text.getText().contains(".")) {
                    e.consume();

                }
            }

        });

    }

    private void soloNum(JTextField text) {
        text.addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c)) {
                    e.consume();
                }
            }

        });

    }

