    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // TODO add your handling code here:
        if (this.nombrePjTextField.getText().equals("") || this.preciojTextField.getText().equals("") || unidadesjTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Llenar campos obligatorios");
        } else {
            this.productoConsul.insertProducto(new Producto(this.nombrePjTextField.getText(), convertidorInt(this.unidadesjTextField.getText()),
                    this.descjTextArea.getText(), this.convertidorDouble(this.preciojTextField.getText()), categoriaSelect()), insertCategoria());
            manejador.manejadorAgotados(productoConsul.productosAgotados(coeficienteAgotamiento), jTableAgotados);
            manejador.manejoInventarioProductos(productoConsul.listaProductos(true, true), InvetarioPjTable);
            this.nombrePjTextField.setText("");
            this.unidadesjTextField.setText("");
            this.preciojTextField.setText("");
            this.descjTextArea.setText("");
        }

    }//GEN-LAST:event_jButtonRegistrarActionPerformed

