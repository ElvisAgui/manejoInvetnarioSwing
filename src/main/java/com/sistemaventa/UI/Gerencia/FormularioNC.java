    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int column = this.jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / this.jTable1.getRowHeight();
        if (row < this.jTable1.getRowCount() && row >= 0 && column < this.jTable1.getColumnCount() && column >= 0) {
            Object value = this.jTable1.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton btn = (JButton) value;
                int seleccion = 1;
                if (btn.getText().equals("Eliminar")) {
                    seleccion = JOptionPane.showOptionDialog(this, "Esta seguro de Eliminar la Categoria?",
                            "Confirmar", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, // null para icono por defecto.
                            null, // null para YES, NO y CANCEL
                            "");
                }
                if (btn.getText().equals("Eliminar") && seleccion == 0) {
                    categConsul.deletCategoria(btn.getName());
                    this.manejadorTable.manejadorTableCategorias(this.categConsul.getCategoriasDB(), jTable1);
                    this.categConsul.enlistarCategorias(categoriasjComboBox, "Categorias");
                    this.categConsul.enlistarCategorias(categoriasOrdejComboBox, "Todos");
                } else if (btn.getText().equals("Editar")) {
                    nameActualizar = categConsul.categoriaEditar(btn.getName(), nameCjTextField, DescripjTextArea);
                    actulizar = true;
                    this.categConsul.enlistarCategorias(categoriasjComboBox, "Categorias");
                    this.categConsul.enlistarCategorias(categoriasOrdejComboBox, "Todos");
                    this.categConsul.enlistarCategorias(categoriaseditjComboBox);

                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

