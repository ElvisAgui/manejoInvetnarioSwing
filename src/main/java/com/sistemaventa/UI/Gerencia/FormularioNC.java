    private void registrarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarjButtonActionPerformed
        // TODO add your handling code here:
        if (this.nameCjTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nombre de la categoria es obligatorio");
        } else {
            if (!actulizar) {
                categConsul.insertCategoria(new Categoria(this.nameCjTextField.getText(), this.DescripjTextArea.getText()));

            } else {
                this.categConsul.updateCategoria(nameActualizar, nameCjTextField.getText(), DescripjTextArea.getText());
                actulizar = false;
            }
            this.manejadorTable.manejadorTableCategorias(categConsul.getCategoriasDB(), jTable1);
            this.categConsul.enlistarCategorias(categoriasjComboBox, "Categorias");
            this.categConsul.enlistarCategorias(categoriasOrdejComboBox, "Todos");
            this.categConsul.enlistarCategorias(categoriaseditjComboBox);
            this.nameCjTextField.setText("");
            this.DescripjTextArea.setText("");
        }

    }//GEN-LAST:event_registrarjButtonActionPerformed

