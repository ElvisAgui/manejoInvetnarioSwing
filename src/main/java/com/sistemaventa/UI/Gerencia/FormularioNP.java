    private void jButtonCrearPerfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearPerfilActionPerformed
        // TODO add your handling code here:

        if (this.nombre.getText().equals("") || this.Pass.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Los campos no puesen estar vacios");
        } else {
            int rol;
            if (this.jComboBoxRol.getSelectedItem().toString().equals("Encargado")) {
                rol = 1;
            } else {
                rol = 2;
            }
            this.userConsul.newEmpleado(nombre.getText(), this.Pass.getText(), rol);
            nombre.setText("");
            Pass.setText("");
            this.manejadorTable.manejadorTableEmpleados(userConsul.listUsuarios(), jTable1);

        }

    }//GEN-LAST:event_jButtonCrearPerfilActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int column = this.jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / this.jTable1.getRowHeight();
        if (row < this.jTable1.getRowCount() && row >= 0 && column < this.jTable1.getColumnCount() && column >= 0) {
            Object value = this.jTable1.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton btn = (JButton) value;
                int seleccion;
                if (btn.getText().equals("Eliminar")) {
                    seleccion = JOptionPane.showOptionDialog(this, "Esta seguro de Eliminar al Usuario?",
                            "Confirmar", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, // null para icono por defecto.
                            null, // null para YES, NO y CANCEL
                            "");
                } else {
                    seleccion = JOptionPane.showOptionDialog(this, "Desea Continuar el Cambio",
                            "Confirmar", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, // null para icono por defecto.
                            null, // null para YES, NO y CANCEL
                            "");

                }
                if (btn.getText().equals("Eliminar") && seleccion == 0) {
                    userConsul.deletUser(btn.getName());
                    this.manejadorTable.manejadorTableEmpleados(userConsul.listUsuarios(), jTable1);

                } else if (btn.getText().equals("Cambiar") && seleccion == 0) {
                    userConsul.updateRol(btn.getName());
                    this.manejadorTable.manejadorTableEmpleados(userConsul.listUsuarios(), jTable1);

                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


